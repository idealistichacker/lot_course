# -*- coding: utf-8 -*-
from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH
import sys

def create_report():
    doc = Document()

    # Title
    heading = doc.add_paragraph()
    heading.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = heading.add_run('物联网与边缘计算（报告）\n基于边缘RAG的海滨阳台气象与资产哨兵系统')
    run.font.size = Pt(16)
    run.font.bold = True

    # Info
    info = doc.add_paragraph()
    info.add_run('成    绩：              \n')
    info.add_run('学生姓名：              \n')
    info.add_run('学    号：              \n')
    info.add_run('专业班级：  计算机2301  \n')
    info.add_run('同组者姓名：                  \n\n')
    info.add_run('2026年 5月20日\n')

    # Abstract
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run('摘  要')
    run.font.size = Pt(14)
    run.font.bold = True

    abstract = ("本项目围绕“海滨阳台无人值守气象与资产哨兵”展开，目标是在宿舍无人照看时，让终端设备自动采集阳台环境和摄像头状态，再由树莓派完成本地知识库检索、推理判断和移动端告警。系统前端感知节点采用 ESP32-S3 CAM 与 DHT11，负责温湿度、露点、Wi-Fi 状态和摄像头初始化信息的上报；树莓派作为边缘计算节点，接收设备数据并生成决策。本报告重点说明本人承担的 ESP32 数据采集与上传部分，尤其是底层传感器的读取重试、网络连接的稳定性保障、数据 JSON 报文的组装以及基于深度休眠的功耗管理体系实现，展示这套终端如何在室外环境中维持长时间的稳定运行。")
    
    p_abs = doc.add_paragraph(abstract)
    p_abs.paragraph_format.first_line_indent = Inches(0.3)
    doc.add_paragraph('关键词：边缘计算；ESP32-S3 CAM；物联网；数据采集；深度休眠')

    # TOC
    p_toc = doc.add_paragraph()
    p_toc.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p_toc.add_run('目  录')
    run.font.size = Pt(14)
    run.font.bold = True

    doc.add_paragraph('摘  要')
    doc.add_paragraph('第1章  引言')
    doc.add_paragraph('第2章  总体设计')
    doc.add_paragraph('第3章  终端硬件初始化与时序配置')
    doc.add_paragraph('第4章  数据采集与露点温度预计算')
    doc.add_paragraph('第5章  网络连接与 JSON 数据上传')
    doc.add_paragraph('第6章  功耗管理与深度休眠调度')
    doc.add_paragraph('第7章  调试与问题处理')
    doc.add_paragraph('第8章  心得体会')

    doc.add_page_break()

    def add_chapter(title, paragraphs):
        h = doc.add_paragraph()
        r = h.add_run(title)
        r.font.size = Pt(14)
        r.font.bold = True
        for para in paragraphs:
            pElem = doc.add_paragraph(para)
            pElem.paragraph_format.first_line_indent = Inches(0.3)

    add_chapter('第1章  引言', [
        "海边宿舍的阳台环境变化比较快，风、湿度、盐雾和短时降雨都可能影响放在外面的物品。项目最初的想法并不是做一个复杂的自动保护装置，而是先把“看见环境、理解风险、及时提醒”这条链路跑通。终端设备在阳台上定时工作，采集温湿度和摄像头状态，并把数据发到局域网中的树莓派。",
        "本次项目中，队友主要完成树莓派端的数据接收和本地知识库建立。我负责的重点则放在 ESP32-S3 CAM 终端侧：一是读取底层传感器数据并进行简单的边缘预计算；二是管理 Wi-Fi 网络的断线重连；三是设计统一格式的数据封包，并将采集到的数据稳定上传；四是控制整个硬件系统的功耗，通过深度休眠实现节律唤醒。两边通过 JSON 报文相衔接，共同构成了完成的闭环。"
    ])

    add_chapter('第2章  总体设计', [
        "系统整体可以分成三层。第一层是感知层，主要由 ESP32-S3 CAM、DHT11 温湿度传感器和摄像头模块组成。终端唤醒后读取湿度并计算露点，检测通讯状态，然后通过网络把数据发出。第二层是边缘计算层，也就是队友负责的树莓派。它运行 Python 程序，接收数据，查询本地知识库并调用大模型。第三层是提醒层，在高风险情况下推送告警给手机。",
        "从数据流看，ESP32 侧上报的数据重点包含了 device_id、seq、temp_c、rh_pct、dew_point_c、wifi_rssi、camera_ok 等字段。我们在设计时弱化了单片机的业务决策能力，这就意味着终端设备只需要老老实实把客观数据和设备自检状态如实记录、打包。只要数据流能无误地送到第二层，感知层的工作就达到了预期。"
    ])

    add_chapter('第3章  终端硬件初始化与时序配置', [
        "在唤醒初始阶段，首要任务是保障程序流的平稳以及生命周期参数的留存。普通全局变量在单片机进入深度休眠或电源复位时会被清除。为了能让树莓派端知道当前设备是在正常工作、还是发生了异常重启，代码中利用了 C 语言的 RTC_DATA_ATTR 宏特性，将 bootCount（重启次数累加器）与 sequenceId（逻辑批次号）存放到了 RTC 内存区。这个区域在深度睡眠时也能保留数据，只有断电才会丢失。这样，每次系统被唤醒，这两个计数器都能提供关键的存活参考线索。",
        "摄像头的初始化同样在这一阶段进行，这也是很多开源代码容易报错的地方。程序通过 camera_config_t 结构体绑定了 XCLK 等引脚，锁定输入频率为 20MHz。考虑到 PSRAM 在不同情况下的不稳定性，程序里特别加了一步探针侦测。如果检测到外部 PSRAM，系统就开启双缓冲机制，支持 VGA 分辨率；如果没有检测到，便降级到单缓冲、CIF 分辨率，避免后续死机重启，大大减少了初始化失败的概率。"
    ])

    add_chapter('第4章  数据采集与露点温度预计算', [
        "由于阳台的环境并不友好，传感器也会出现各种突发性的小故障。 DHT11 是一款十分常用的通信单总线传感器，但只要时序稍微有毫秒级的偏离，或者有电磁干扰，读出的数据就会失效，返回 NaN（非数字）。如果我们的程序只读一次就走，很容易就把 NaN 传给了树莓派，导致错误报警。",
        "为了解决读取成功率问题，代码中封装了一个 readDHTWithRetry() 重试函数。逻辑是这样的：如果拿不到正常数值，或者相对湿度不在 0 到 100 之间，程序会延时 1500 毫秒让传感器元件恢复，然后再读一次。这个重试池最大有 5 次尝试机会。这样处理以后，上报的数据几乎不再出现无效值。",
        "另外为了减少树莓派的计算负担，发挥边缘设备的预处理能力，我在 ESP32 上面补充了露点温度的工程近似计算逻辑。海滨阳台上的许多物品并不是只怕相对湿度，而是更怕温度降至露点之下，导致结露。程序利用当前的温度和湿度数值直接算出近似露点。这虽是个简单的边缘处理，但让后端直接接收到了安全指标数据，省去了后端频繁调用转换函数的开销。"
    ])

    add_chapter('第5章  网络连接与 JSON 数据上传', [
        "网络初始化时，程序先让 ESP32 执行一次环境 Wi-Fi 扫描，把扫描到的各个路由器 SSID 和信号强度打印在串口，方便我们判断布放位置的真实信号环境。",
        "成功连接并且获取局域网 IP 后，我就利用底层的 snprintf 函数将搜集到的温度、湿度、存活毫秒数、硬件指示系统信息拼接到一起，构成 768 大小的固定字符缓冲带。我没有引入第三方厚重的解析库，这样既缩减了编译后的二进制体积，又彻底切断了意外内存碎片堆积泄漏崩溃问题。",
        "发送方面目前使用的是 HTTPClient 库发出 POST 请求。我在原来只发一次的设计基础上加了三阶重试功能。在阳台弱网环境下偶尔提交失败太常见了，如果 httpCode 没有正常返回或断开，代码会暂停等待一段时间再重发，直到确认上传成功，确保每次环境数据都能稳当落入树莓派的服务接收口中。"
    ])

    add_chapter('第6章  功耗管理与深度休眠调度', [
        "如果不作专门的节电控制，让 ESP32 持续开启内部的 Wi-Fi 射频模块空转，很快就会耗尽阳台备用电池的电量。实际的阳台环境监测也不需要高频度刷新上报。所以在执行完发送指令之后，最重要的一步就是马上进入关机省电状态。",
        "发完数据后代码会强制执行 WiFi.disconnect(true) 和 WiFi.mode(WIFI_OFF) ，从物理底层切断整个基带放大器的供电损耗。紧随其后调用系统级命令 esp_deep_sleep_start()，在这之前会装载好一个 15 分钟为标准单位的唤醒定时器。这就让 ESP32 在剩余的大多数休整时长里，所有内部高频硅区域全部切断了工作流，功耗也断崖式压缩到了微安级别。"
    ])

    add_chapter('第7章  调试与问题处理', [
        "在跟队友进行两端合并联调时，出现了一些协议上的分歧与棘手问题。",
        "第一个问题是由两边的网络通信协议差异引发的。项目规划中，队友经常用 MQTT 客户端做本地小样测试；但在封装项目时，我们大家确立采用更为常见的 HTTP /upload 短传格式。好在我们在设计报文规范时提前统一了全部业务标识字段，所以我发出 POST 包请求之后，队友只需要增加一个接口中转层提取这部分 JSON 字段做拆分平移转换就能兼容，并未再大动核心判定代码分毫。",
        "第二个比较严重的绊脚石是偶发性的硬件崩溃。起初阶段融合了摄像机外设之后，插电唤醒时 Wi-Fi 搜网和 DHT 管脚数据的捕获如果在由于中断时同时启动，很容易产生抢夺 CPU 时钟从而出现 NaN 报错死机。找出病灶之后，我在逻辑里做出了改版分离，把传感器读工作放在纯净电平环境下去先独立执行到位，等稳稳采集到了气象结果才开通信网络连结，大大降低了并发现象激活的各种冲突。"
    ])

    add_chapter('第8章  心得体会', [
        "做完了这个感知采集子端，使得我对物联网中的“边缘计算”与“工程兜底”有了极度具体的现实映射。边缘计算迫切要先摆在台面攻克的恰恰是真实世界的信号采集、电耗计算与断连信号这种工程基建问题。ESP32 异常适合扮演一线轻骑兵，用来专注环境抓捕与局部边缘计算后立时深度休眠；然后交给能加载复杂模型树莓派进行判断，这让整个流程成为完整的闭环。",
        "回顾我这段工程的代码，它的每一行防御退缩修正代码结构如加入进去的 RTC 维护代码、五轮次补偿重连等，无一不在抵御阳台复杂气候下带来的不可抗拒因素。正因前线哨兵顶住了巨大压力，从而令系统最终顺利给出了关窗收衣服这样的实用建议，这不仅在工程层面上磨练了我的技术边界，更在思想层面上激起了巨大的成就感。这非常鼓励我在以后的迭代中进一步利用好现有的 camera_ok 将图像采集彻底打通并运用大模型来升级项目。"
    ])

    doc.save('物联网与边缘计算报告_esp32数据采集上传部分.docx')

if __name__ == '__main__':
    create_report()
